class Character
   def initialize(name, strength, dexterity, constitution, intelligence, wisdom, charisma, alignment, cclass, race)
	@name = name
	@strength = strength
	@dexterity = dexterity
	@constitution = constitution
	@intelligence = intelligence
	@wisdom = wisdom
	@charisma = charisma
	@alignment = alignment
	@cclass = cclass
	@race = race
	end
  end
